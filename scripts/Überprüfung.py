# Wörter, die geklammert werden sollen
zu_klammern = {"vor", "der", "Tür", "stehen"}

# Textzeilen einlesen
lines = text.strip().split('\n')
output_lines = []

# durch alle Zeilen gehen
for line in lines:
    neue_wörter = []
    for wort in line.split():
        wort_clean = wort.strip(".,!?;:")  # Satzzeichen entfernen zur Prüfung
        if wort_clean in zu_klammern:
            neue_wörter.append(f"[{wort}]")
        else:
            neue_wörter.append(wort)
    output_lines.append(" ".join(neue_wörter))

# Ausgabe der neuen Zeilen
for line in output_lines:
    print(line)


